package gda.data.structure;

import java.io.ByteArrayInputStream;
import java.io.ByteArrayOutputStream;
import java.io.ObjectInputStream;
import java.io.ObjectOutputStream;

import org.slf4j.Logger;
import org.slf4j.LoggerFactory;

/**
 * ObjectCloner Class
 */
public class ObjectCloner {
	private static final Logger logger = LoggerFactory.getLogger(ObjectCloner.class);

	// so that nobody can accidentally create an ObjectCloner object
	private ObjectCloner() {
	}

	/**
	 * Returns a deep copy of an object.
	 * 
	 * @param oldObj
	 * @return Object
	 * @throws Exception
	 */
	static public Object deepCopy(Object oldObj) throws Exception {
		ObjectOutputStream oos = null;
		ObjectInputStream ois = null;
		try {
			ByteArrayOutputStream bos = new ByteArrayOutputStream(); // A
			oos = new ObjectOutputStream(bos); // B
			// serialize and pass the object
			oos.writeObject(oldObj); // C
			oos.flush(); // D
			ByteArrayInputStream bin = new ByteArrayInputStream(bos.toByteArray()); // E
			ois = new ObjectInputStream(bin); // F
			// return the new object
			return ois.readObject(); // G
		} catch (Exception e) {
			logger.debug("Exception in ObjectCloner = " + e);
			throw (e);
		} finally {
			if (oos != null)
				oos.close();
			if (ois != null)
				ois.close();
		}
	}

}
