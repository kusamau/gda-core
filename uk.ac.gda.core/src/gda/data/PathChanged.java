package gda.data;

import java.io.Serializable;

/**
 * Pushes out a changed path
 */
public class PathChanged implements Serializable {

	private final String path;

	public PathChanged(String newpath) {
		path = newpath;
	}

	public String getPath() {
		return path;
	}

	@Override
	public int hashCode() {
		// Auto generated by Eclipse IDE
		final int prime = 31;
		int result = 1;
		result = prime * result + ((path == null) ? 0 : path.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		// Auto generated by Eclipse IDE
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		PathChanged other = (PathChanged) obj;
		if (path == null) {
			if (other.path != null)
				return false;
		} else if (!path.equals(other.path))
			return false;
		return true;
	}

	@Override
	public String toString() {
		return "PathChange(" + path + ")";
	}

}
